class Producto {
    id;
    nombre;
    descripcion;
    precio;
    imagen;
    cantidad;

    constructor(id, nombre, descripcion, precio, imagen, cantidad) {
    this.id = id;
    this.nombre = nombre;
    this.descripcion = descripcion;
    this.precio = precio;
    this.imagen = imagen;
    this.cantidad = cantidad;
    }}